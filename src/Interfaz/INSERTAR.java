   public static void obtenerDatos() {
    conector c = new conector();
    Connection con = c.conexion();
    String sql = "SELECT CONCAT(Nombre1, ' ', Nombre2) AS Nombres, CONCAT(apellido1, ' ', apellido2) AS Apellidos, fecha_nacimiento, fecha_mat FROM estudiantes INNER JOIN matricula ON estudiantes.N_matricula = matricula.N_matricula;";
    Statement stmt;
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("Nombre");
    model.addColumn("Apellido");
    model.addColumn("Fecha de nacimiento");
    model.addColumn("Fecha de matr√≠cula");
    jTable1.setModel(model);
    String[] datos = new String[4];
    try {
        stmt = con.createStatement();
        ResultSet rs = stmt.executeQuery(sql);
        while (rs.next()) {
            datos[0] = rs.getString(1);
            datos[1] = rs.getString(2);
            datos[2] = rs.getDate(3).toString();
            datos[3] = rs.getDate(4).toString();
            model.addRow(datos);
        }
        con.close();
    } catch (SQLException e) {
        e.printStackTrace();
    }
}

    

